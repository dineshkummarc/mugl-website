	<h2>July 14, 2011: Inaugural MUGL Meeting, John Snelson</h2>

	<p class="homepagetext">
		The very first meeting of the MUGL took place 
		on July 14, 2011 at the Skills Matter Exchange.
	</p>
	<p class="homepagetext">
		By all accounts it was a great success.  We had
		attendance of about 20 people, from a variety of
		companies around London that are using MarkLogic
		in many different ways.  Not bad considering we did
		very little promotion.  We even had a couple of
		senior MarkLogic people from The States drop by
		who were in London for other reasons.
	</p>
	<p class="homepagetext">

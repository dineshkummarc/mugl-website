	<p class="homepagetext">
		arkLogic is a commercial software product sold by 
		MarkLogic Corporation, which is based in California.
	</p>
	<p class="homepagetext">
		MarkLogic's primary product, MarkLogic Server, is best
		described as a combination XML datastore and search engine.
		It can store very large amounts of XML, 100s of terabytes,
		and query it very quickly.
	</p>
	<p class="homepagetext">
		MarkLogic indexes and stores XML in a transactional way,
		which is why it's often called an "XML database".  Rather
		that SQL, MarkLogic used the XQuery language to query and
		transform stored XML.
	</p>
	<p class="homepagetext">
		The bit that make MarkLogic unique among XML storage engines
		is that it also includes powerful search engine capabilities
		for querying the text of the stored XML.  Search is
		transactional with content storage, which means that as
		soon as a document is committed to the store, it is
		immediately visible for search.
	</p>
	
